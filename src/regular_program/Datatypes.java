package regular_program;

public class Datatypes {
	public static void main(String[] args)

	{
		// integer types
		
		// byte stored value to 127
		byte a=100;
		System.out.println(a);
		
		// short stored value to 32767
		short b=5000;
		System.out.println(b);
		
		// int stored value to 10 digits
		int c=45858456;
		System.out.println(c);
		
		//long stored value to large numbers
		long d=1546835456684L;
		System.out.println(d);
		
		// float types
		float e=234.43f;
		System.out.println(e);
		
		// double types
		double f=14585.47d;
		System.out.println(f);
		
		// char types
	    char vt='A';
	    System.out.println(vt);
		
		//boolean types
		boolean abc=true;
		System.out.println(abc);
		
		boolean xyz=false;
		System.out.println(xyz);
		
		// String types
		String name="Hello World";
		System.out.println(name);
}

}

