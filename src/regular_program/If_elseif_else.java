package regular_program;

public class If_elseif_else {
	public static void main(String[] args) {
		int a=0;
		if (a<0) {
			System.out.println("a is less than 0.");
		}
		else if (a>0) {
			System.out.println("a is greater than 0.");
		}
		else {
			System.out.println("a is Equal to zero.");
		}
	}

}
