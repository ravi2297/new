package regular_program;

public class Variables_properties {
	public static void main(String[] args)
	{
		
	// you can also assign the value letter.	
	 int myNum;          // variable declaration____
	 myNum=45;           // variable intialization______
	 System.out.println(myNum);
	 
	 // Change the db from 487 to 500.
	 int db=487;
	 db=500;
	 System.out.println(db);
	 
	 // final variable so no one can overwrite existing value name
	 final int bb=56;
	 System.out.println(bb);
	 
	 final String name="rahul";
	 System.out.println(name);
	 
	 // you can also add one variable to another.
	 String Animal="Dog";
	 String Sentence="is a pet animal.";
	 System.out.println(Animal + "\n" + "\t" + Sentence);
	 
	 // declare more than one value with same type
	 int x=25,y=25,z=50;
	 System.out.println(x+y+z);
	 
	 
	 
	 
	 
	 
		
		
	}

}
