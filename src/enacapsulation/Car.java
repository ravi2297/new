package enacapsulation;

public class Car {

	private String name;
	private int price;
	private String speed;
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name=name;
	}
	
	public int getPrice()
	{
		return price;
	}
	
	public void setPrice(int price)
	{
		this.price=price;
	}
	
	public String getSpeed()
	{
		return speed;
	}
	
	public void setSpeed(String speed)
	{
		this.speed=speed;
	}
}
