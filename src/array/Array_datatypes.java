package array;

public class Array_datatypes {
	

public static void main(String[] args) 
		{
	
	// array help us to store multiple elements of same data types.

	int [] age=new int[8];
		age[0]=21;
		age[1]=22;
		age[2]=23;
		age[3]=21;
		age[4]=20;
		age[5]=19;
		age[6]=18;
		age[7]=19;
		
		for (int a:age) {
			System.out.println(a);
		}
	}

}
